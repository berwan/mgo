use graph;
use io;


function debug(f, d0=null, d1=null, d2=null, d3=null, d4=null)
   // DEBUG
   graph.font(["SwissA", 16, true, false]);
   graph.pen(graph.white);
   graph.brush(graph.white);
   graph.rect(60, 277, 120, 25);
   graph.pen(graph.black);

   l = .len(.split(f, "%"))-1;

   case l
      in 1:
         print io.format("dbg: " + f, d0);
         graph.text(60, 290, io.format("dbg: " + f, d0));
      in 2:
         print io.format("dbg: " + f, d0, d1);
         graph.text(60, 290, io.format("dbg: " + f, d0, d1));
      in 3:
         print io.format("dbg: " + f, d0, d1, d2);
         graph.text(60, 290, io.format("dbg: " + f, d0, d1, d2));
      in 4:
         print io.format("dbg: " + f, d0,d1,d2,d3);
         graph.text(60, 290, io.format("dbg: " + f, d0,d1,d2,d3));
      in 5:
         print io.format("dbg: " + f, d0,d1,d2,d3,d4);
         graph.text(60, 290, io.format("dbg: " + f, d0,d1,d2,d3,d4));
   end;
   graph.show();
   // DEBUG
end

